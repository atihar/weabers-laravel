                                    <b>Continue with support and maintenance</b><br>
                                    Your work isn’t over after you’ve released and marketed your app. You’re now responsible for its maintenance and support. 

                                    Maintaining means releasing updates, monitoring performance, and fixing anything that might hurt the user experience of your app. 
                                    App maintenance is critically important because it allows you to fix any issues and gives you a chance to add new features so your app can stand out from the competition. 
                                    The way you maintain your app will have a direct impact on whether your project is a success or a failure in the long run.
                                    <br><br>
                                    A report from Upland found that 71% of users uninstall the app within the first 3 months of its release because of negligence and failure to enhance the user experience. 
                                    In order to achieve a large number of active users and keep your app up and running well after 3-6 months post-release, have a support and maintenance strategy ready before you launch the app. 
