                                    Without a great feedback collection system, you miss out on learning opportunities critical to the success of your app. If you want to adapt to the expectations of your users in a timely manner, you need your feedback system to be quick, relevant, and uninterrupted.
                                    Since demanding user feedback is often frustrating, you have to make sure your process of gathering feedback does not hurt the user experience. 
